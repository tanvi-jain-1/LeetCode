class Solution{
public:
    int sumOfDigits(int N){
        int k=N;
        
        int sum=0;
        while(k>0)
        {
            int last_digit=k%10;
            k=k/10;
            sum+=last_digit;
        }
        return sum;
        //code here
    }
};

